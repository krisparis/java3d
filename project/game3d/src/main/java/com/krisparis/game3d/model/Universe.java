package com.krisparis.game3d.model;

/**
 * <p>
 * Represents the universe and defines physical constants
 * such as the values of the celerity or the gravity.
 * </p>
 */
public class Universe {
    
    /**
     * <p>
     * The value of the speed of light in meter per second as defined
     * in US-1.
     * </p>
     */
    public static final int CELERITY = 299792458;    
    
    /**
     * <p>
     * The value of the gravity in m/(s^2) as defined
     * in US-1.
     * </p>
     */
    public static final float GRAVITY = 9.81f;
    
    /**
     * <p> Private constructor.</p>
     */
    private Universe() {
    }
    
}
